    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(jTextIdSocio.getText().isEmpty()&& !jTextNombre.getText().isEmpty() && !jTextdni.getText().isEmpty()){
            if(jTextNombre.getText().matches("[a-zA-Z]")){
               guardar(); 
            }else{
                JOptionPane.showMessageDialog(this, "Nombre inválido", "Información", JOptionPane.ERROR_MESSAGE);
            }
            
        }else if (!jTextNombre.getText().isEmpty() && !jTextdni.getText().isEmpty()){
            if(jTextNombre.getText().matches("[a-zA-Z]")){
            actualizar();
            }else{
                JOptionPane.showMessageDialog(this, "Nombre inválido", "Información", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Debes llenar los campos", "Ingormación", JOptionPane.ERROR_MESSAGE);
        }
        limpiar();
        cargarTabla();
    }//GEN-LAST:event_btnGuardarActionPerformed

