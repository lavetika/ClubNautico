    private void buscar(String texto){
       
       ArrayList<Socio> socios = sociosBD.buscar(texto);        
        DefaultTableModel modelo = (DefaultTableModel)jTable1.getModel();        
        modelo.setRowCount(0);
        for (Socio socio: socios) {
            Object[] fila = new Object[3];
            fila[0] = socio.getIdSocio();
            fila[1] = socio.getDni();
            fila[2] = socio.getNombre();
            modelo.addRow(fila);
        }
    }
    private void guardar(){
        String nombre = jTextNombre.getText();
        String dni = jTextdni.getText();
        Socio socio = new Socio(dni, nombre);
        sociosBD.guardar(socio);
    }
    private void mostrarInfo(){
        int indiceFila = jTable1.getSelectedRow();
        int idSocio = (Integer)jTable1.getValueAt(indiceFila, 0);
            Socio socio = sociosBD.consultar(idSocio);
            jTextIdSocio.setText(socio.getIdSocio()+"");
            jTextNombre.setText(socio.getNombre());
            jTextdni.setText(socio.getDni());
            jTextdni.setEnabled(false);
            jTextNombre.setEnabled(false);
    }
    private void eliminar(){
        int indiceFila = jTable1.getSelectedRow();
        if(indiceFila == -1){
            JOptionPane.showMessageDialog(this, "Debes seleccionar un socio", "Información", JOptionPane.ERROR_MESSAGE);
        }else{
            int idSocio = (Integer)jTable1.getValueAt(indiceFila, 0);
            Socio socio = sociosBD.consultar(idSocio);
            sociosBD.eliminar(idSocio);
            limpiar();
        }
        
    }
    
    private void editar(){
        int indiceFila = jTable1.getSelectedRow();
        if(indiceFila == -1){
            JOptionPane.showMessageDialog(this, "Debes seleccionar un socio", "Información", JOptionPane.ERROR_MESSAGE);
        }else{
            mostrarInfo();
            jTextdni.setEnabled(true);
            jTextNombre.setEnabled(true);
        }
    }
    
    private void limpiar(){
        jTextIdSocio.setText("");
        jTextNombre.setText("");
        jTextdni.setText("");
    }
    
    private void actualizar(){
        int idSocio = Integer.parseInt(jTextIdSocio.getText());
        String nombre = jTextNombre.getText();
        String dni = jTextdni.getText();
        Socio socio = new Socio(idSocio, dni, nombre);
        sociosBD.actualizar(socio);
    }
    
    private void cargarTabla(){
        String textoBusqueda = "";
        ArrayList<Socio> socios = sociosBD.consultar();        
        DefaultTableModel modelo = (DefaultTableModel)jTable1.getModel();        
        modelo.setRowCount(0);
        for (Socio socio: socios) {
            Object[] fila = new Object[3];
            fila[0] = socio.getIdSocio();
            fila[1] = socio.getDni();
            fila[2] = socio.getNombre();
            modelo.addRow(fila);
        }
    }

